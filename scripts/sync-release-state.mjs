#!/usr/bin/env node
/**
 * Deterministic release-state sync for reference docs.
 *
 * Reads `docs/releases/current.json` (the committed source of truth) plus
 * machine-derived facts (HEAD commit, test count, DD count), then rewrites
 * the `<!-- release-state -->` fenced blocks in each reference doc with
 * consistent values.
 *
 * Usage:
 *   node scripts/sync-release-state.mjs              # update all reference docs
 *   node scripts/sync-release-state.mjs --check      # verify sync (exit 1 if drift)
 *   node scripts/sync-release-state.mjs --dry-run    # show what would change
 *
 * Reference docs must contain a fenced block:
 *
 *   <!-- release-state -->
 *   **Version:** 0.12.0-preview.2 | **Dist-Tag:** next | ...
 *   <!-- /release-state -->
 *
 * Everything between the markers is replaced deterministically.
 * Commentary outside the markers is left untouched.
 *
 * Design: one canonical source (current.json) + machine-derived facts.
 * No hand-maintained approximate values. No seven parallel manual edits.
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFileSync } from 'node:child_process';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

const args = process.argv.slice(2);
const checkOnly = args.includes('--check');
const dryRun = args.includes('--dry-run');

// --- Read manifest ---
const manifestPath = join(ROOT, 'docs/releases/current.json');
if (!existsSync(manifestPath)) {
  console.error('ERROR: docs/releases/current.json not found');
  process.exit(1);
}
const manifest = JSON.parse(readFileSync(manifestPath, 'utf-8'));

// --- Machine-derived facts ---
function getHeadCommit() {
  try {
    return execFileSync('git', ['rev-parse', '--short=8', 'HEAD'], {
      cwd: ROOT,
      encoding: 'utf-8',
    }).trim();
  } catch {
    return 'unknown';
  }
}

function getHighestDD() {
  const ddLog = join(ROOT, 'reference/PEAC_NORMATIVE_DECISIONS_LOG_LOCAL.md');
  if (!existsSync(ddLog)) return 0;
  const content = readFileSync(ddLog, 'utf-8');
  const matches = content.match(/DD-(\d+)/g);
  if (!matches) return 0;
  return Math.max(...matches.map((m) => parseInt(m.replace('DD-', ''), 10)));
}

function getNodeVersion() {
  try {
    return execFileSync('node', ['-v'], { encoding: 'utf-8' }).trim();
  } catch {
    return 'unknown';
  }
}

const head = getHeadCommit();
const highestDD = getHighestDD();
const nodeVersion = getNodeVersion();
const today = new Date().toISOString().split('T')[0];

// --- Build the replacement block ---
const stateBlock = [
  `**Version:** ${manifest.version} | **Wire:** ${manifest.wire_format_version} | **Dist-Tag:** \`${manifest.dist_tag}\``,
  `**HEAD:** \`${head}\` | **Node:** ${nodeVersion} | **DDs:** ${highestDD}`,
  `**Errors:** ${manifest.errors_version} | **Registries:** ${manifest.registries_version}`,
  `**Synced:** ${today} (machine-generated by \`scripts/sync-release-state.mjs\`)`,
].join('\n');

// --- Find and update reference docs ---
const START_MARKER = '<!-- release-state -->';
const END_MARKER = '<!-- /release-state -->';

const refDir = join(ROOT, 'reference');
const targetFiles = [
  'RELEASE_STATE_LOCAL.md',
  'ROADMAP_LOCAL.md',
  'SCOPE_LEDGER_LOCAL.md',
  'MASTER_PLAN_LOCAL.md',
  'AGENT_GTM_LOCAL.md',
  'QUICK_START_LOCAL.md',
  'ARCHITECTURE_LOCAL.md',
];

let updated = 0;
let drifted = 0;

for (const fileName of targetFiles) {
  const filePath = join(refDir, fileName);
  if (!existsSync(filePath)) {
    console.log(`  SKIP: ${fileName} (not found)`);
    continue;
  }

  const content = readFileSync(filePath, 'utf-8');
  const startIdx = content.indexOf(START_MARKER);
  const endIdx = content.indexOf(END_MARKER);

  if (startIdx === -1 || endIdx === -1) {
    console.log(`  SKIP: ${fileName} (no release-state markers; add them to enable sync)`);
    continue;
  }

  const before = content.substring(0, startIdx + START_MARKER.length);
  const after = content.substring(endIdx);
  const newContent = `${before}\n${stateBlock}\n${after}`;

  if (newContent === content) {
    console.log(`  OK: ${fileName} (in sync)`);
    continue;
  }

  drifted++;
  if (checkOnly) {
    console.log(`  DRIFT: ${fileName}`);
  } else if (dryRun) {
    console.log(`  WOULD UPDATE: ${fileName}`);
  } else {
    writeFileSync(filePath, newContent);
    console.log(`  UPDATED: ${fileName}`);
    updated++;
  }
}

console.log('');
if (checkOnly) {
  if (drifted > 0) {
    console.log(`${drifted} file(s) drifted. Run: node scripts/sync-release-state.mjs`);
    process.exit(1);
  }
  console.log('All synced files are up to date.');
} else if (dryRun) {
  console.log(`Would update ${drifted} file(s).`);
} else {
  console.log(`Updated ${updated} file(s).`);
}
